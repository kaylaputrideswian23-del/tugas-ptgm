#operator bitwise

a = 6
b = 3

# bitwise OR (&)
c = a & b
print('nilai :',a,)
print('nilai :',b,)
print('nilai bitwise AND :',c,)

# bitwise OR (|)
c = a | b
print('nilai :',a,)
print('nilai :',b,)
print('nilai bitwise OR :',c,)

# bitwise XOR (^)
c = a ^ b
print('nilai :',a,)
print('nilai :',b,)
print('nilai bitwise XOR :',c,)

# bitwise NOT (~)
c = ~a
c = ~b
print('nilai :',a,)
print('nilai :',b,)
print('nilai bitwise NOT :',c,)

# shift right (>>)
c = a >> 2
print('nilai :',a,)
print('nilai :',b,)
print('nilai shift right :',c,)

# shift left (<<)
c = a << 2
print('nilai :',a,)
print('nilai :',b,)
print('nilai shift left :',c,)